    POTENCIA_MOTOR DECIMAL(2,1),
    CAMBIO VARCHAR(255),
    DIRECAO VARCHAR(255),
    COR VARCHAR(255),
    UNICO_DONO VARCHAR(10),
    OPCIONAIS VARCHAR(255),
    GNV VARCHAR(10),
    REVISOES VARCHAR(10),
    GARANTIA VARCHAR(10),
    LEILAO VARCHAR(10),
    IPVA_PAGO VARCHAR(10),
